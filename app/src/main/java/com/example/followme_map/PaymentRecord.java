package com.example.followme_map;

public class PaymentRecord {
    public String time;
    public String place;
    public String price;
    public String date;

    public void setPlace(String place) {
        this.place = place;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
